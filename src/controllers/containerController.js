var containerModel = require("../models/containerModel");

function testar(req, res) {
    console.log("ENTRAMOS NO containerontroller");
    res.send("ENTRAMOS NO COntainer CONTROLLER");
}

function listar(req, res) {
    containerModel.listar().then(function (resultado) {
        if (resultado.length > 0) {
            res.status(200).json(resultado);
        } else {
            res.status(204).send("Nenhum resultado encontrado!")
        }
    }).catch(function (erro) {
        console.log(erro);
        console.log("Houve um erro ao buscar os avisos: ", erro.sqlMessage);
        res.status(500).json(erro.sqlMessage);
    });
}

function listarPorTipo(req, res) {
    var tipo = req.params.tipo;
    var fkEmpresa = req.params.fkEmpresa

    containerModel.listarPorTipo(tipo, fkEmpresa)
        .then(
            function (resultado) {
                if (resultado.length > 0) {
                    res.status(200).json(resultado);
                } else {
                    res.status(204).send("Nenhum resultado encontrado!");
                }
            }
        )
        .catch(
            function (erro) {
                console.log(erro);
                console.log(
                    "Houve um erro ao buscar os avisos: ",
                    erro.sqlMessage
                );
                res.status(500).json(erro.sqlMessage);
            }
        );
}

